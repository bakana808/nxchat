package com.hyperfresh.mchyperchat.theme;

public enum ThemeElement
{
	/**
	 * The format used at the beginning of a user's
	 * chain of chat messages.
	 */
	CHAT_HEADER_FORMAT,

	/**
	 * The format used in a user's chat messages.
	 */
	CHAT_MESSAGE_FORMAT,

	/**
	 * The format used at the end of a user's
	 * chain of chat messages.
	 */
	CHAT_FOOTER_FORMAT
}
